package com.netfinworks.basis.inf.ucs.support.annotation.advice;

/**
 * 缓存默认配置
 * @author bigknife
 *
 */
public class CacheDefaultConfig {
	private CacheDefaultConfig(){}
	private static CacheDefaultConfig instance = new CacheDefaultConfig();
	public static CacheDefaultConfig getInstance(){
		return instance;
	}
	
	private int defaultExpireSecond;
	/**
	 * @return the defaultExpireSecond
	 */
	public int getDefaultExpireSecond() {
		return defaultExpireSecond;
	}

	/**
	 * @param defaultExpireSecond the defaultExpireSecond to set
	 */
	public void setDefaultExpireSecond(int defaultExpireSecond) {
		this.defaultExpireSecond = defaultExpireSecond;
	}
	
}
